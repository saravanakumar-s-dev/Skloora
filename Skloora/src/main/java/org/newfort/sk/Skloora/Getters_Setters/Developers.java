package org.newfort.sk.Skloora.Getters_Setters;

public class Developers 
{

    private String dev_name;
    private String dev_des;
    private String dev_email;
    public String getDev_name() {
        return dev_name;
    }
    public void setDev_name(String dev_name) {
        this.dev_name = dev_name;
    }
    public String getDev_des() {
        return dev_des;
    }
    public void setDev_des(String dev_des) {
        this.dev_des = dev_des;
    }
    public String getDev_email() {
        return dev_email;
    }
    public void setDev_email(String dev_email) {
        this.dev_email = dev_email;
    }

    

}
